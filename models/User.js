import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
  fullnames: {
    type: String,
    required: true,
    unique: true,
  },
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    minLength: 6,
  },
});
export default mongoose.model("User", UserSchema);
